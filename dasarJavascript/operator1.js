// Operator Aritmatika (Arithmetic)

let a = 2, b = 5, c = "10"
console.log("a + b = " + (a+b) ); // Operasi penjumlahan 
console.log("a + c = " + (a+c) ); // Operasi penggabungan (concat)
console.log("a - b = " + (a-b) ); // Operasi pengurangan
console.log("a * b = " + (a*b) ); // Operasi perkalian
console.log("b / a = " + (b/a) ); // Operasi pembagian
console.log("b % a = " + (b%a) ); // Operasi sisa hasil bagi
console.log("b ** a = " + (b**a) ); // Operasi decrement (pengurangan nilai 1)