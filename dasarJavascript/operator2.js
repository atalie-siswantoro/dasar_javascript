// Operator Penugasan
// Example

let a = 3, b = 4

console.log("a = "+ (a += b)); 
// Jadi untuk variable a akan berubah dengan hasil dari operasi tambah tersebut

console.log("b = "+ (b -= a)); 
// Jadi untuk variable b akan berubah dengan hasil dari operasi kurang tersebut

console.log("b = "+ (b *= a)); 
// Jadi untuk variable b akan berubah dengan hasil dari operasi kali tersebut